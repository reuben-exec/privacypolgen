# Process each locale
for locale in ['de', 'es', 'fr', 'ar', 'ko', 'ja', 'zh', 'ru', 'pt', 'hi', 'ta', 'ka', 'vi', 'nl', 'tr', 'bn']:
    filepath = os.path.join(LOCALES_DIR, f'{locale}.json')
    with open(filepath, 'r', encoding='utf-8') as f:
        locale_data = json.load(f)

    # Get translations for this locale, fallback to English
    locale_translations = TRANSLATIONS.get(locale, {})

    # Find missing keys
    en_flat = flatten(en)
    locale_flat = flatten(locale_data)
    missing = set(en_flat.keys()) - set(locale_flat.keys())

    if missing:
        # For keys we have translations for, use them
        # For keys we don't have translations for, use the English value (will be replaced later by native speakers)
        additions = {}
        for key in sorted(missing):
            if key in locale_translations:
                additions[key] = locale_translations[key]
            else:
                additions[key] = en_flat[key]  # Fallback to English

        additions_structured = unflatten(additions)
        locale_data = deep_merge(locale_data, additions_structured)

    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(locale_data, f, indent=2, ensure_ascii=False)
        f.write('\n')

    # Verify
    new_flat = flatten(locale_data)
    still_missing = set(en_flat.keys()) - set(new_flat.keys())
    print(f'{locale}.json: {len(missing)} keys added, {len(still_missing)} still missing, {len(new_flat)} total')

print('\nDone!')

